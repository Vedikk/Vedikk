

function fadeIn (){
    document.getElementById('search').style.display='inline-block';
    document.getElementById('fa-search').style.display='none';
}
function fadeOut (){
    document.getElementById('search').style.display='none';
    document.getElementById('fa-search').style.display='inline-block';
}
function rightIn(){
    document.getElementById('drop_menu').style.display='block';
    document.getElementById('bars').style.display='none';
}
function rightOut(){
    document.getElementById('drop_menu').style.display='none';
    document.getElementById('bars').style.display='inline-block';
}

